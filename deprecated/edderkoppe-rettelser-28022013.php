Det fører jer til systemets default forside, klik på "Søg i Danmarks Edderkopper" igen for at komme tilbage.

Søg nogle edderkoppefund frem. Ude til højre i tabellen er der nu et blyant-ikon. Det er til rettelser på selve fundet. Det skal jeg nok gøre pænere, men det er konceptet (har i den her øvelse lavet noglet helt generelt det er meningen skal genbruges på tværs)

Det nye er redigering i arts-teksterne, klik på et edderkoppe-ikon (til venstre) for at komme til en artsside. 
Som logget ind vil der nu over rammen være en "Rediger"-knap. Klikker I på den kommer i til en side hvor I kan redigere teksterne for den aktuelle art, hhv species, genus og family (fanebladstitlerne er Jørgen Lissners tabel-navne, for at I kan se hvad det handler om)

De enkelte poster læses ind i deres fulde omfang, der vises en rich text editor ud for de felter der havde memo / text typer i Jørgens Lissners oprindelige database.

Jeg håber det kan bruges, eller I kan visualisere hvordan det kan ende ud ;-)

